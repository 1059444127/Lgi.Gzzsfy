        {
            get
            {
                var lb = this.F_BRLB.Replace("内镜", "").Replace(")", "").Replace("(", "").Replace("（", "").Replace("）", "");
                switch (lb)
                {
                    case "门诊":return BRLB.门诊;
                    case "住院":return BRLB.住院;
                    case "体检":return BRLB.体检;
                    case "急诊":return BRLB.急诊;
                    case "转院": return BRLB.转院;
                    default:
                        return BRLB.未知;

                }
            }
        }

